static uint16_t led_timer;
bool            led_layer_state = false;

// Blink at layer 1, 2 or 3
void matrix_scan_user(void) {
    uint8_t layer   = biton32(layer_state);
    uint8_t usb_led = host_keyboard_leds();

    if (timer_elapsed(led_timer) > LAYER_BLINK_TIME) {
        switch (layer) {
            case 1:
                if (!led_layer_state) {
                    led_set(0);
                } else {
                    led_set(4);
                }

                led_layer_state = !led_layer_state;
                break;
            case 2:
                if (!led_layer_state) {
                    led_set(0);
                } else {
                    led_set(2);
                }

                led_layer_state = !led_layer_state;
                break;
            case 3:
                if (!led_layer_state) {
                    led_set(0);
                } else {
                    led_set(6);
                }

                led_layer_state = !led_layer_state;
                break;
            case 4:
                if (!led_layer_state) {
                    led_set(0);
                } else {
                    led_set(1);
                }

                led_layer_state = !led_layer_state;
                break;
            case 5:
                if (!led_layer_state) {
                    led_set(0);
                } else {
                    led_set(5);
                }

                led_layer_state = !led_layer_state;
                break;
            case 6:
                if (!led_layer_state) {
                    led_set(0);
                } else {
                    led_set(3);
                }

                led_layer_state = !led_layer_state;
                break;
            case 7:
                if (!led_layer_state) {
                    led_set(0);
                } else {
                    led_set(7);
                }

                led_layer_state = !led_layer_state;
                break;
            default:
                led_set(usb_led);
                led_layer_state = false;
        }

        led_timer = timer_read();
    }
};

bool tb_disable_multiplier = false;
bool tb_scroll = false;

bool process_record_user(uint16_t keycode, keyrecord_t *record) {

    switch (keycode) {
        case TBMUL:
            if (record->event.pressed) {
                tb_disable_multiplier = true;
            } else {
                tb_disable_multiplier = false;
            }
            return false;
        case TBMUL_TOG:
            if (record->event.pressed) {
                tb_disable_multiplier = !tb_disable_multiplier;
            }
            return false;
        case TBSCRL:
            if (record->event.pressed) {
                tb_scroll = true;
            } else {
                tb_scroll = false;
            }
            return false;
        case TBSCRL_TOG:
            if (record->event.pressed) {
                tb_scroll = !tb_scroll;
            }
            return false;
    }

    return true;
}

void ps2_mouse_moved_user(report_mouse_t *mouse_report) {
    if (tb_disable_multiplier) {
        mouse_report->x = mouse_report->x / (PS2_MOUSE_X_MULTIPLIER);
        mouse_report->y = mouse_report->y / (PS2_MOUSE_Y_MULTIPLIER);
    }

    if (tb_scroll) {
        mouse_report->v = -mouse_report->y / (PS2_MOUSE_SCROLL_DIVISOR_V);
        mouse_report->h = mouse_report->x / (PS2_MOUSE_SCROLL_DIVISOR_H);
        mouse_report->x = 0;
        mouse_report->y = 0;
    }
}

void raw_hid_receive_kb(uint8_t *data, uint8_t length) {
    if(data[0] == 0xFC){
        switch (data[1]) {
            case 0x01:{
                // move to layer
                data[1] = 0xFD;
                layer_move(data[2]);
                break;
            }
            case 0x02:{
                // turn on layer
                data[1] = 0xFD;
                layer_on(data[2]);
                break;
            }
            case 0x03:{
                // turn off layer
                data[1] = 0xFD;
                layer_off(data[2]);
                break;
            }
        }
    }
}

uint8_t last_layer_state = 0;

layer_state_t layer_state_set_user(layer_state_t state) {
    uint8_t data[32] = {0};
    data[0] = 0xFC;
    data[1] = 0xFD;
    data[2] = biton32(state);

    // Layer change
    if (last_layer_state != data[2]) {
        raw_hid_send(data, 32);

        last_layer_state = data[2];
    }

    return state;
}
